import { styles } from "./styles";
import { MaterialIcons } from "@expo/vector-icons";
import { Text, TouchableOpacity, View } from "react-native";
import { TransactionTypes } from "@/utils/TransactionTypes";
import { colors } from "@/theme";

export type TransactionProps = {
    id: string
    value: string
    date: string
    description?: string
    type: TransactionTypes
};

type Props = {
    data: TransactionProps;
    onRemove(): void;
};

export function Transaction({ data, onRemove }: Props) {
    return (
        <View style={styles.container}>
            <MaterialIcons
                color={data.type === TransactionTypes.Input ? colors.blue[500] : colors.red[400]}
                name={data.type === TransactionTypes.Input ? "arrow-upward" : "arrow-downward"}
                size={20}/>
            <View style={styles.info}>
                <Text style={styles.value}>{data.value}</Text>
                <Text
                    numberOfLines={1}
                    style={styles.description}>
                    {data.date} {data.description && `â€¢ ${data.description}`}
                </Text>
            </View>
            <TouchableOpacity
                activeOpacity={0.8}
                onPress={onRemove}>
                <MaterialIcons
                    color={colors.gray[500]}
                    name="close"
                    size={18}/>
            </TouchableOpacity>
        </View>
    );
}